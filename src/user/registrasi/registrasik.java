    private void butdaftarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butdaftarMouseClicked
        // TODO add your handling code here:
        if (txtnama.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Masih Kosong !!");
            txtnama.requestFocus();
        }
        else if(txtnohp.getText().equals("")){
            JOptionPane.showMessageDialog(null,"No HP Masih Kosong !!");
            txtnohp.requestFocus();            
        }
        else if(txtalamat.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Alamat Masih Kosong !!");
            txtalamat.requestFocus();            
        }
        else if(txtusername.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Username Masih Kosong !!");
            txtusername.requestFocus();            
        }
        else if(txtpassword.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Password Masih Kosong !!");
            txtpassword.requestFocus();            
        }
        else if(txtkonfirmasipassword.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Konfirmasi Passnya Masih Kosong !!");
            txtkonfirmasipassword.requestFocus();
        }
        else{
            //new Dashframe().setVisible(true);
            //this.dispose(); 
        }
        String nama = txtnama.getText();
        String nohp = txtnohp.getText();
        String alamat = txtalamat.getText();
        String id = idfield.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();
        String konpassword = txtkonfirmasipassword.getText();
        
        if (!password.equals(konpassword)){
            JOptionPane.showMessageDialog(null, "Password Tidak Sama");    
        }else if (password.equals("")|| username.equals("")){
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
        }else{
            try{
                Connection c = Kaweshop.getkawe();
                String sql = "INSERT INTO member VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, id);
                p.setString(2, nama);
                p.setString(3, alamat);
                p.setString(4, nohp);
                p.setString(5, username);
                p.setString(6, password);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dibuat");
            }catch(SQLException ex){
                System.out.println("erur");
            }finally{
                this.setVisible(false);
                
            }
        }
    }//GEN-LAST:event_butdaftarMouseClicked


