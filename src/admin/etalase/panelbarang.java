    protected void datatable(){
        Object[] Baris = {"ID Barang", "Nama Barang", "Kategori Barang","Harga", "Stok"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();            
        try{
            String sql = "SELECT * FROM barang WHERE id_barang like '%"+cariitem+"%' or nama_barang like '%"+cariitem+"%' order by id_barang asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
              
                });
            }
            tablebarang.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal di panggil"+e);
        }
    }
        private void tampilData(){
        //untuk mengahapus baris setelah input
        int row = tablebarang.getRowCount();
        for(int a = 0 ; a < row ; a++){
            table.removeRow(0);
        }
    
        String query = "SELECT * FROM barang ";
        
        try{
            Connection connect = Kaweshop.getkawe();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            while (rslt.next()){
                //menampung data sementara
                   
                    String kode = rslt.getString("id_barang");
                    String nama = rslt.getString("nama_barang");
                    String harga = rslt.getString("harga");
                    String stok = rslt.getString("stok");
                    
                //masukan semua data kedalam array
                String[] data = {kode,nama,harga,stok};
                //menambahakan baris sesuai dengan data yang tersimpan diarray
                table.addRow(data);
            }
                //mengeset nilai yang ditampung agar muncul di table
                tablebarang.setModel(table);
            
        }catch(Exception e){
            System.out.println(e);
        }
        }
